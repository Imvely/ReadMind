"""핵심 문장 추출 프롬프트 (명세서 §5.5).

LLM이 고른 문장은 서비스에서 원문 대조로 검증하므로, 프롬프트는 "원문 그대로
복사"를 강하게 요구한다(환각·변형 방지).
"""

from __future__ import annotations

_SYSTEM = (
    "너는 학술/기술 문서에서 핵심 문장을 골라내는 추출기다. "
    "주어진 본문에서 가장 중요한 문장들을 원문 그대로(축약·수정·번역 없이) 복사한다. "
    "각 문장에 대해 왜 중요한지 한 문장으로 설명을 붙인다. "
    "아래 JSON 객체 하나만 출력한다(코드펜스/추가텍스트 없이):\n"
    '{"highlights": [{"text": "원문 문장 그대로", "reason": "왜 중요한지"}, ...]}\n'
    "text는 반드시 본문에 등장하는 문장과 글자 그대로 일치해야 한다. 지어내지 않는다."
)


def system() -> str:
    return _SYSTEM


def user(text: str, want: int) -> str:
    return (
        f"다음 본문에서 가장 중요한 문장 약 {want}개를 원문 그대로 골라 "
        f"JSON으로 출력하라.\n본문:\n{text}"
    )
